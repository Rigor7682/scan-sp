    [Parameter()]
    [string]$TenantAdminUrl,

    [Parameter()]
    [string]$OutputPath = ".\SPPermissions_$(Get-Date -Format 'yyyyMMdd_HHmmss').html",

    [Parameter()]
    [ValidateSet("Site", "SiteAndLibraries", "Full")]
    [string]$ScanDepth = "SiteAndLibraries",
    # Site              : Site uniquement (permissions du site)
    # SiteAndLibraries  : Site + Listes/Bibliotheques (recommande, rapide)
