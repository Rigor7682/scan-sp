<#
.SYNOPSIS
    SharePoint Permission Scanner - Generates a detailed HTML report of all permissions
    across SharePoint sites, libraries, folders, and files.

.DESCRIPTION
    This script connects to SharePoint Online using PnP PowerShell and recursively
    scans permissions at all levels: Site, Lists/Libraries, Folders, and Files.
    It detects broken inheritance and exports a rich interactive HTML report.

.PARAMETER Mode
    Scanning mode:
      - SingleSite     : Scan a single SharePoint site
      - AllSites       : Scan all sites in the tenant
      - SiteCollection : Scan a specific site collection (root + subsites)

.PARAMETER SiteUrl
    URL of the SharePoint site (required for SingleSite and SiteCollection modes).

.PARAMETER TenantAdminUrl
    URL of the SharePoint Admin Center (e.g. https://contoso-admin.sharepoint.com).
    Required for AllSites mode.

.PARAMETER OutputPath
    Path and filename for the HTML report. Defaults to .\SPPermissions_<timestamp>.html

.PARAMETER IncludeFiles
    If set, scans individual file permissions (slower but more thorough). Default: $true

.PARAMETER MaxFileDepth
    Maximum folder depth to scan for files. Default: 10

.PARAMETER ExcludeSystemLists
    Exclude SharePoint system lists (Style Library, Site Assets, etc.). Default: $true

.PARAMETER ClientId
    Azure AD App Registration Client ID for app-only authentication.

.PARAMETER Thumbprint
    Certificate thumbprint for app-only authentication.

.PARAMETER Tenant
    Tenant ID or domain for app-only authentication.

.EXAMPLE
    # Interactive login - Single site
    .\SPPermissionScanner.ps1 -Mode SingleSite -SiteUrl "https://contoso.sharepoint.com/sites/HR"

.EXAMPLE
    # All sites in tenant
    .\SPPermissionScanner.ps1 -Mode AllSites -TenantAdminUrl "https://contoso-admin.sharepoint.com"

.EXAMPLE
    # App-only auth - Site collection
    .\SPPermissionScanner.ps1 -Mode SiteCollection -SiteUrl "https://contoso.sharepoint.com/sites/Projects" `
        -ClientId "xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx" `
        -Thumbprint "ABCDEF1234567890" `
        -Tenant "contoso.onmicrosoft.com"

.NOTES
    Author  : Generated by Claude
    Version : 1.0
    Requires: PnP.PowerShell module (Install-Module PnP.PowerShell)
    
    Required permissions (App-only):
      - Sites.FullControl.All (SharePoint)
      - Directory.Read.All (Graph - for user/group details)
#>

[CmdletBinding()]
