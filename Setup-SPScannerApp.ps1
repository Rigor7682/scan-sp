function Write-Log {
    param([string]$Message, [string]$Level = "INFO")
    $timestamp = Get-Date -Format "HH:mm:ss"
    $color = switch ($Level) {
        "INFO"    { "Cyan" }
        "OK"      { "Green" }
        "WARN"    { "Yellow" }
        "ERROR"   { "Red" }
        "SECTION" { "Magenta" }
        default   { "White" }
    }
    Write-Host "[$timestamp] " -NoNewline -ForegroundColor DarkGray
    Write-Host "[$Level] " -NoNewline -ForegroundColor $color
    Write-Host $Message
}

function Add-Result {
    param(
        [string]$SiteUrl,
        [string]$SiteName,
        [string]$ObjectType,   # Site | Library | List | Folder | File
        [string]$ObjectName,
        [string]$ObjectUrl,
        [bool]$HasUniquePermissions,
        [string]$InheritedFrom,
        [PSCustomObject[]]$Permissions
    )
    $Script:ScanResults.Add([PSCustomObject]@{
        SiteUrl              = $SiteUrl
        SiteName             = $SiteName
        ObjectType           = $ObjectType
        ObjectName           = $ObjectName
        ObjectUrl            = $ObjectUrl
        HasUniquePermissions = $HasUniquePermissions
        InheritedFrom        = $InheritedFrom
        Permissions          = $Permissions
        ScannedAt            = (Get-Date -Format "o")
    })
    $Script:TotalScanned++
}



