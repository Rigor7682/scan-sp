try {
    # Validate parameters
    if ($Mode -eq "SingleSite" -and -not $SiteUrl)       { throw "SiteUrl is required for SingleSite mode." }
    if ($Mode -eq "SiteCollection" -and -not $SiteUrl)   { throw "SiteUrl is required for SiteCollection mode." }
    if ($Mode -eq "AllSites" -and -not $TenantAdminUrl)  { throw "TenantAdminUrl is required for AllSites mode." }

    # Show auth params for troubleshooting
    Write-Log "Auth params received :" -Level INFO
    Write-Log "  ClientId   = '$ClientId'" -Level INFO
    Write-Log "  Thumbprint = '$Thumbprint'" -Level INFO
    Write-Log "  Tenant     = '$Tenant'" -Level INFO

    # Check certificate in store if thumbprint provided
    if ($Thumbprint) {
        $certUser    = Get-Item "Cert:\CurrentUser\My\$Thumbprint"  -ErrorAction SilentlyContinue
        $certMachine = Get-Item "Cert:\LocalMachine\My\$Thumbprint" -ErrorAction SilentlyContinue
        if ($certUser)    { Write-Log "Certificate found in CurrentUser\My store." -Level OK }
        elseif ($certMachine) { Write-Log "Certificate found in LocalMachine\My store." -Level OK }
        else {
            Write-Log "Certificate NOT found in any store! Thumbprint: $Thumbprint" -Level WARN
            Write-Log "Run: Import-PfxCertificate -FilePath '<path>.pfx' -CertStoreLocation Cert:\CurrentUser\My" -Level WARN
        }
    }

    # Check PnP module
    # Verifier PnP.PowerShell
    if (-not (Get-Module -ListAvailable -Name "PnP.PowerShell")) {
        Write-Log "PnP.PowerShell non installe. Lancez : Install-Module PnP.PowerShell -Scope CurrentUser" -Level ERROR
        exit 1
    }
    # Importer PnP proprement en evitant les conflits d assemblies Microsoft.Graph
    # Si Microsoft.Graph SDK est deja charge dans la session, PnP peut entrer en conflit.
    # Solution : ne pas importer explicitement (PnP.PowerShell s auto-importe via ses cmdlets)
    # et ignorer les erreurs d assembly non critiques.
    if (-not (Get-Module -Name "PnP.PowerShell")) {
        try {
            Import-Module PnP.PowerShell -ErrorAction Stop -WarningAction SilentlyContinue
            Write-Log "PnP.PowerShell charge." -Level OK
        } catch {
            if ($_.Exception.Message -like "*Assembly*already loaded*" -or
                $_.Exception.Message -like "*Could not load file or assembly*") {
                Write-Log "Avertissement assembly Graph (non bloquant) - le script continue." -Level WARN
            } else {
                Write-Log "Erreur chargement PnP.PowerShell : $_" -Level ERROR
                Write-Log "Conseil : Ouvrez une nouvelle session PowerShell et relancez le script." -Level WARN
                exit 1
            }
        }
    }

    # Choix interactif du ScanDepth si pas specifie (valeur par defaut)
    if (-not $PSBoundParameters.ContainsKey('ScanDepth')) {
        Write-Host ""
        Write-Host "  Profondeur de scan :" -ForegroundColor Cyan
        Write-Host "  [1] Site uniquement                          (tres rapide)" -ForegroundColor White
        Write-Host "  [2] Site + Bibliotheques/Listes              (rapide - recommande)" -ForegroundColor White
        Write-Host "  [3] Site + Bibliotheques + Dossiers/Fichiers (lent)" -ForegroundColor White
        Write-Host ""
        Write-Host "  Votre choix [1/2/3] (defaut: 2) : " -NoNewline -ForegroundColor Yellow
        $choice = Read-Host
        $ScanDepth = switch ($choice) {
            "1" { "Site" }
            "3" { "Full" }
            default { "SiteAndLibraries" }
        }
        Write-Log "ScanDepth selectionne : $ScanDepth" -Level OK
    }

    # Orchestration du scan
    Write-Log "═══════════════════════════════════════════════" -Level SECTION
    Write-Log "  SharePoint Permission Scanner  v1.0" -Level SECTION
    Write-Log "  Mode      : $Mode" -Level SECTION
    Write-Log "  ScanDepth : $ScanDepth" -Level SECTION
    Write-Log "═══════════════════════════════════════════════" -Level SECTION

    switch ($Mode) {
        "SingleSite" {
            Invoke-ScanSite -Url $SiteUrl
        }
        "SiteCollection" {
            Connect-ToSite -Url $SiteUrl
            $subsiteUrls = @(Get-PnPSubWeb -Recurse | Select-Object -ExpandProperty Url)
            Disconnect-PnPOnline
            Invoke-ScanSite -Url $SiteUrl
            foreach ($subUrl in $subsiteUrls) { Invoke-ScanSite -Url $subUrl }
        }
        "AllSites" {
            if (-not $TenantAdminUrl) { throw "TenantAdminUrl is required for AllSites mode." }
            Connect-ToSite -Url $TenantAdminUrl
            $allSites = Get-PnPTenantSite -IncludeOneDriveSites:$false | Select-Object -ExpandProperty Url
            Disconnect-PnPOnline
            Write-Log "Found $($allSites.Count) sites to scan." -Level OK
            foreach ($siteUrl in $allSites) { Invoke-ScanSite -Url $siteUrl }
        }
    }

    Write-Log "Scan complete. Total objects scanned: $Script:TotalScanned" -Level OK
    New-HtmlReport

    Write-Log "═══════════════════════════════════" -Level SECTION
    Write-Log "  Done! Report: $OutputPath" -Level OK
    Write-Log "═══════════════════════════════════" -Level SECTION


    # Ouvrir le rapport - uniquement en mode interactif
    # En mode non-interactif (Start-SPScanner.ps1), on saute cette etape
    if ([Environment]::UserInteractive -and -not ([Console]::IsInputRedirected)) {
        try {
            $openReport = Read-Host "Open report in browser? (Y/N)"
            if ($openReport -eq "Y") {
                $absPath = (Resolve-Path $OutputPath -ErrorAction SilentlyContinue | Select-Object -ExpandProperty Path -ErrorAction SilentlyContinue)
                if (-not $absPath) { $absPath = $OutputPath }
                Start-Process "explorer.exe" -ArgumentList "`"$absPath`""
            }
        } catch { }
    }

} catch {
    Write-Log "Fatal error: $_" -Level ERROR
    Write-Log $_.ScriptStackTrace -Level ERROR
    exit 1
}

