function Connect-ToSite {
    param([string]$Url)
    Write-Log "Connecting to: $Url" -Level INFO
    try {
        if ($ClientId -and $ClientSecret -and $Tenant) {
            # Authentification par Client Secret (pas de certificat requis)
            Write-Log "Auth: Client Secret" -Level INFO
            Connect-PnPOnline -Url $Url -ClientId $ClientId -ClientSecret $ClientSecret -Tenant $Tenant -ErrorAction Stop
        } elseif ($ClientId -and $Thumbprint -and $Tenant) {
            # Verify certificate exists in store
            $cert = Get-Item "Cert:\CurrentUser\My\$Thumbprint" -ErrorAction SilentlyContinue
            if (-not $cert) {
                $cert = Get-Item "Cert:\LocalMachine\My\$Thumbprint" -ErrorAction SilentlyContinue
            }
            if (-not $cert) {
                throw "Certificate with thumbprint '$Thumbprint' not found in CurrentUser\My or LocalMachine\My store. Import the .pfx file first: Import-PfxCertificate -FilePath '<path>.pfx' -CertStoreLocation Cert:\CurrentUser\My"
            }
            Write-Log "Certificate found: $($cert.Subject) (expires $($cert.NotAfter.ToString('yyyy-MM-dd')))" -Level OK
            # App-only auth with certificate thumbprint
            Connect-PnPOnline -Url $Url `
                -ClientId $ClientId `
                -Thumbprint $Thumbprint `
                -Tenant $Tenant `
                -ErrorAction Stop
        } elseif ($ClientId -and $Tenant) {
            # App-only auth - interactive device login
            Connect-PnPOnline -Url $Url `
                -ClientId $ClientId `
                -Tenant $Tenant `
                -Interactive `
                -ErrorAction Stop
        } else {
            # Delegated interactive login (uses PnP community app)
            Connect-PnPOnline -Url $Url -Interactive -ErrorAction Stop
        }
        Write-Log "Connected successfully." -Level OK
    } catch {
        Write-Log "Connection failed: $_" -Level ERROR
        throw
    }
}

